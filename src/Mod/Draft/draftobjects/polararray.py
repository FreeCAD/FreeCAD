import FreeCAD as App
import Draft


def make_polar_array(obj,
                     center=App.Vector(0, 0, 0), angle=180, number=4,
                     use_link=False):
    """Create a polar array from the given object.
    """
    obj = Draft.makeArray(obj,
                          arg1=center, arg2=angle, arg3=number,
                          use_link=use_link)
    return obj
