# CL-point filtering via LineCLFilter
# ---------------------------------------------------------------------------


def filter_cl_points(cl_points, tolerance):
    """Use OCL LineCLFilter to remove collinear CL-points.

    Replaces the manual ``isOnLineSegment`` checks and
    ``OptimizeLinearPaths`` logic currently in SurfaceSupport.py.

    Args:
        cl_points: List of ``(x, y, z)`` tuples.
        tolerance: Collinearity tolerance (mm).

    Returns:
        Filtered list of ``(x, y, z)`` tuples.
    """
    ocl = _get_ocl()

    if len(cl_points) < 3:
        return list(cl_points)

    f = ocl.LineCLFilter()
    f.setTolerance(tolerance)

    for pt in cl_points:
        f.addCLPoint(ocl.CLPoint(pt[0], pt[1], pt[2]))

    filter_start = time.perf_counter()
    f.run()
    filter_time = time.perf_counter() - filter_start

    result = [(cl.x, cl.y, cl.z) for cl in f.getCLPoints()]

    Path.Log.debug(
        "filter_cl_points: {} -> {} points in {:.3f}s".format(
            len(cl_points), len(result), filter_time
        )
    )

    return result


# ---------------------------------------------------------------------------
# Optimize the path
# ---------------------------------------------------------------------------


def _optimize_travel(
    last_point,
    next_point,
    start_z,
    safe_z,
    step_down,
    clearance_z,
    horiz_feed,
    horiz_rapid,
    vert_rapid,
    safe_pdc=None,
    cutter=None,
    force_keep_down=False,
):
    """Find the shortest safe path between two scan line endpoints.

    For short transitions (≤ 2× cutter diameter), emits a direct G1
    feed move to the next point — the tool stays down without probing.
    Longer transitions fall back to a full ``safe_z`` retract.

    Long-distance optimization is deferred to a future iteration.

    Args:
        last_point: (x, y, z) end of previous scan line.
        next_point: (x, y, z) start of next scan line.
        start_z: Start Depth.
        safe_z: Safe retract height.
        step_down: Step Down height
        clearance_z: Clearance height.
        horiz_feed: Horizontal cutting feed rate.
        horiz_rapid: Horizontal rapid rate.
        vert_rapid: Vertical rapid rate.
        safe_pdc: Pre-built ``ocl.PathDropCutter`` (from
                  :func:`_make_safe_pdc`).  Reused across transitions.
        cutter: OCL cutter (needed for diameter threshold).
        force_keep_down: Force KeepToolDown for ZigZag cut patterns

    Returns:
        List of ``Path.Command``.
    """
    if safe_pdc is not None and cutter is not None:
        cutter_diam = cutter.getDiameter()
        dx = next_point[0] - last_point[0]
        dy = next_point[1] - last_point[1]
        xy_dist_sqrd = dx * dx + dy * dy

        if xy_dist_sqrd <= (cutter_diam * 2.0) ** 2:
            transition_cmds = _dropcutter_transition(
                last_point,
                next_point,
                safe_pdc,
                start_z,
                safe_z,
                step_down,
                horiz_feed,
                force_keep_down,
            )
            if transition_cmds:
                return transition_cmds

    # Fallback: full retract
    return [
        Path.Command("G0", {"Z": safe_z, "F": vert_rapid}),
        Path.Command("G0", {"X": next_point[0], "Y": next_point[1], "F": horiz_rapid}),
    ]


def _dropcutter_transition(
    start, end, safe_pdc, start_z, safe_z, step_down, horiz_feed, force_keep_down=False
):
    """Probes a transition path and returns surface-following G1 commands."""
    ocl = _get_ocl()
    path = ocl.Path()
    # Create a line high above the part for the drop-cutter to start from
    p1 = ocl.Point(start[0], start[1], safe_z)
    p2 = ocl.Point(end[0], end[1], safe_z)
    path.append(ocl.Line(p1, p2))

    safe_pdc.setPath(path)
    safe_pdc.run()

    cl_points = safe_pdc.getCLPoints()
    if len(cl_points) < 2:
        return None

    # We only perform the aggressive climb check if we are NOT being forced to stay down.
    if not force_keep_down:
        first_transition_pt = cl_points[1]
        initial_climb = abs(first_transition_pt.z - start[2])
        step_over_transition = abs(start[2] - end[2]) < 1e-3
        above_start_z = start[2] > start_z

        # If the initial climb is more than half a step-down, it's faster to retract.
        if (
            step_down > 0 and initial_climb > (step_down / 2.0) and not step_over_transition
        ) or above_start_z:
            Path.Log.debug(
                f"Keep-down move aborted: initial climb ({initial_climb:.2f}mm) exceeds safety check."
            )
            return None  # Abort the transition

    # Use a math-based "z-floor" to prevent the tool from diving into holes or off edges
    z_floor = min(start[2], end[2])
    commands = []

    # Generate G1 moves that follow the probed surface
    for pt in cl_points[1:-1]:  # Skip first and last point to avoid duplicating moves
        z = max(pt.z, z_floor)  # Plus a small buffer to avoid touching previous on Multi-pass
        commands.append(Path.Command("G1", {"X": pt.x, "Y": pt.y, "Z": z, "F": horiz_feed}))

    # Ensure a perfect final connection to the start of the next cutting pass
    commands.append(Path.Command("G1", {"X": end[0], "Y": end[1], "Z": end[2], "F": horiz_feed}))
    return commands


def _make_safe_pdc(safe_stl, cutter, final_z, sample_interval):
    """Create a reusable PathDropCutter for transition probing."""
    ocl = _get_ocl()
    safe_pdc = ocl.PathDropCutter()
    safe_pdc.setSTL(safe_stl)
    safe_pdc.setCutter(cutter)
    safe_pdc.setZ(final_z)
    safe_pdc.setSampling(sample_interval)
    return safe_pdc


# ---------------------------------------------------------------------------
# G-code generation from drop-cutter data
# ---------------------------------------------------------------------------


def scan_lines_to_gcode(
    scan_lines,
    horiz_feed,
    vert_rapid,
    horiz_rapid,
    safe_z,
    step_down,
    sample_interval,
    clearance_z,
    start_z,
    final_z,
    depth_offset=0.0,
    optimize_transitions=False,
    safe_stl=None,
    cutter=None,
    force_keep_down=False,
):
    """Convert multiple scan lines of CL-points to G-code with transitions.

    Each scan line is a list of ``(x, y, z)`` tuples.  Travel moves are
    inserted between scan lines.

    Args:
        scan_lines: List of scan lines, each a list of ``(x, y, z)`` tuples.
        horiz_feed: Horizontal feed rate.
        vert_rapid: Vertical rapid feed rate.
        horiz_rapid: Horizontal rapid feed rate.
        safe_z: Safe height.
        step_down: Step Down height
        sample_interval: Sample_interval.
        clearance_z: Clearance height.
        start_z: Start Depth.
        final_z: Final depth.
        depth_offset: Optional Z offset.
        optimize_transitions: If True, short transitions (≤ 2× cutter
                              diameter) use a direct G1 feed move instead
                              of retracting.  Longer transitions fall back
                              to a full safe_z retract.
        safe_stl: Optional ``ocl.STLSurf`` for transition optimization.
        cutter: Optional OCL cutter for transition optimization.

    Returns:
        List of ``Path.Command``.
    """

    if not scan_lines:
        return []

    # Create the PDC once and reuse it for all transitions
    safe_pdc = None
    if optimize_transitions and safe_stl is not None and cutter is not None:
        safe_pdc = _make_safe_pdc(safe_stl, cutter, final_z, sample_interval)

    commands = []
    commands.append(Path.Command("G0", {"Z": clearance_z, "F": vert_rapid}))

    last_point = None

    for line_idx, line in enumerate(scan_lines):
        if not line:
            continue

        first_point = line[0]

        # Travel to start of this scan line
        if last_point is not None:
            if optimize_transitions:
                travel_cmds = _optimize_travel(
                    last_point,
                    first_point,
                    start_z,
                    safe_z,
                    step_down,
                    clearance_z,
                    horiz_feed,
                    horiz_rapid,
                    vert_rapid,
                    safe_pdc,
                    cutter,
                    force_keep_down=False,
                )
            else:
                travel_cmds = [
                    Path.Command("G0", {"Z": safe_z, "F": vert_rapid}),
                    Path.Command(
                        "G0",
                        {"X": first_point[0], "Y": first_point[1], "F": horiz_rapid},
                    ),
                ]
            commands.extend(travel_cmds)
        else:
            commands.append(
                Path.Command(
                    "G0",
                    {"X": first_point[0], "Y": first_point[1], "F": horiz_rapid},
                )
            )

        # Cut along this scan line
        for pt in line:
            z = pt[2] + depth_offset
            commands.append(Path.Command("G1", {"X": pt[0], "Y": pt[1], "Z": z, "F": horiz_feed}))

        last_point = line[-1]

    return commands
