# Apply Multi-pass roughing
# ---------------------------------------------------------------------------


def apply_multipass(scan_lines, start_depth, final_depth, step_down):
    """
    Splits and clamps full-depth scan lines into multiple Z-levels for roughing.

    Args:
        scan_lines: List of scan lines, each a list of (x,y,z) tuples at full depth.
        start_depth: Top Z level to start cutting.
        final_depth: Final target Z level.
        step_down: Maximum depth per pass.

    Returns:
        A flat list of scan lines ordered by layer.
    """
    if step_down <= 0.0 or final_depth >= start_depth:
        return scan_lines

    # Generate depth levels from top to bottom
    depthparams = []
    curr_z = start_depth - step_down
    while curr_z > final_depth + 1e-5:
        depthparams.append(curr_z)
        curr_z -= step_down

    # Ensure absolute final depth is always the last pass
    if not depthparams or abs(depthparams[-1] - final_depth) > 1e-5:
        depthparams.append(final_depth)

    all_multipass_lines = []

    for i, layDep in enumerate(depthparams):
        prvDep = start_depth if i == 0 else depthparams[i - 1]

        for line in scan_lines:
            current_segment = []
            last_pt_above = None

            for pt in line:
                x, y, z_original = pt

                # A point is "above" if it was fully machined in a previous pass
                # (- 1e-4 ensures flat floors cut on the previous pass are successfully skipped here)
                is_above = z_original >= prvDep - 1e-4

                if is_above:
                    if current_segment:
                        # We were cutting, and now the tool is moving out of the cutting zone.
                        # We append the original, unclamped 3D point to the segment.
                        current_segment.append((x, y, z_original))
                        all_multipass_lines.append(current_segment)
                        current_segment = []  # Reset segment safely
                    last_pt_above = (x, y, z_original)

                else:
                    # We are IN the cutting zone (z < prvDep - 1e-4)
                    if not current_segment and last_pt_above:
                        # We just dropped into the cutting zone.
                        # Start with the last point above to ramp in seamlessly
                        current_segment.append(last_pt_above)

                    clamped_z = max(z_original, layDep)
                    current_segment.append((x, y, clamped_z))

            # End of the line - if we have an active cutting segment, save it
            if current_segment:
                all_multipass_lines.append(current_segment)

    return all_multipass_lines


