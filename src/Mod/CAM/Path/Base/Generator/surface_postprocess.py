"""
Toolpath Post-Processing and G-Code Generation for the 3D Surface Operation.

This module contains all the functions responsible for taking the raw, 3D point
data generated by the OCL drop-cutter engines and transforming it into a final,
optimized toolpath.

The pipeline is as follows:
1.  Apply Multi-pass logic to slice the toolpath into Z-layers.
2.  Filter out single-point Z-spikes caused by vertical wall artifacts.
3.  Optimize the path by removing redundant co-linear points.
4.  Generate the final G-code, including optimized, collision-safe transition moves.
"""

