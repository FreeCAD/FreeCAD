"""
Agent Qt slots / widgets that must stay uncompiled

QObject subclasses and Qt signal machinery do not survive Cython compilation,
so they live here.

Do NOT edit this file, it backs the signed-in status widget and may destroy functionality.
"""

import json
import threading

from PySide6 import QtCore


class AgentAuthBridge(QtCore.QObject):
    def __init__(self, parent=None):
        super().__init__(parent)
        self._lock = threading.Lock()
        self._payload = ""

    @QtCore.Slot(result=str)
    def current(self):
        with self._lock:
            return self._payload

    def set_session(self, payload):
        with self._lock:
            self._payload = payload or ""


class _AuthPoller:
    def __init__(self, bridge, provider):
        self._bridge = bridge
        self._provider = provider
        self._stop = threading.Event()
        self._thread = None

    def start(self):
        if self._thread is not None:
            return
        self._thread = threading.Thread(
            target=self._run, name="agent-auth", daemon=True
        )
        self._thread.start()

    def stop(self):
        self._stop.set()

    def _run(self):
        while not self._stop.is_set():
            text = ""
            try:
                payload = self._provider()
            except Exception:
                payload = None
            if payload:
                try:
                    text = json.dumps(payload)
                except (TypeError, ValueError):
                    text = ""
            self._bridge.set_session(text)
            self._stop.wait(2.0)


def create_agent_auth_bridge(provider):
    bridge = AgentAuthBridge()
    poller = _AuthPoller(bridge, provider)
    return bridge, poller.start, poller.stop
