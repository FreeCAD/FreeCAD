"""
Agent Qt slots / widgets that must stay uncompiled

QObject subclasses and Qt signal machinery do not survive Cython compilation,
so they live here.

Do NOT edit this file, it backs the signed-in status widget and may destroy functionality.
"""

import json
import threading

from PySide6 import QtCore


_SENSITIVE_AUTH_KEYS = {
    "access_token",
    "refreshToken",
    "refresh_token",
    "idToken",
    "id_token",
    "token",
}


def _sanitize_auth_payload(payload):
    if isinstance(payload, dict):
        return {
            key: _sanitize_auth_payload(value)
            for key, value in payload.items()
            if key not in _SENSITIVE_AUTH_KEYS
        }
    if isinstance(payload, list):
        return [_sanitize_auth_payload(value) for value in payload]
    return payload


class AgentAuthBridge(QtCore.QObject):
    def __init__(self, parent=None):
        super().__init__(parent)
        self._lock = threading.Lock()
        self._payload = ""

    @QtCore.Slot(result=str)
    def current(self):
        with self._lock:
            return self._payload

    def set_session(self, payload):
        with self._lock:
            self._payload = payload or ""


class _AuthPoller:
    def __init__(self, bridge, provider):
        self._bridge = bridge
        self._provider = provider
        self._stop = threading.Event()
        self._thread = None

    def start(self):
        if self._thread is not None:
            return
        self._thread = threading.Thread(
            target=self._run, name="agent-auth", daemon=True
        )
        self._thread.start()

    def stop(self):
        self._stop.set()

    def _run(self):
        while not self._stop.is_set():
            text = ""
            try:
                payload = self._provider()
            except Exception:
                payload = None
            if payload:
                try:
                    text = json.dumps(_sanitize_auth_payload(payload))
                except (TypeError, ValueError):
                    text = ""
            self._bridge.set_session(text)
            self._stop.wait(2.0)


class SignalForwarder(QtCore.QObject):
    def __init__(self, callback, parent=None):
        super().__init__(parent)
        self._callback = callback
        self._signal = None
        self._slot = None

    @QtCore.Slot()
    def call(self):
        self._callback()

    @QtCore.Slot(str)
    def call_str(self, value):
        self._callback(value)

    @QtCore.Slot(object)
    def call_object(self, value):
        self._callback(value)

    def bind(self, signal, slot):
        self._signal = signal
        self._slot = slot
        signal.connect(slot)
        return self

    def disconnect(self):
        if self._signal is None or self._slot is None:
            return
        try:
            self._signal.disconnect(self._slot)
        except (RuntimeError, TypeError):
            pass
        self._signal = None
        self._slot = None


def connect(signal, callback, arg=None):
    forwarder = SignalForwarder(callback)
    if arg == "str":
        slot = forwarder.call_str
    elif arg == "object":
        slot = forwarder.call_object
    elif arg is None:
        slot = forwarder.call
    else:
        raise ValueError(f"Unsupported Agent signal argument type: {arg}")
    return forwarder.bind(signal, slot)


def create_agent_auth_bridge(provider):
    bridge = AgentAuthBridge()
    poller = _AuthPoller(bridge, provider)
    return bridge, poller.start, poller.stop
