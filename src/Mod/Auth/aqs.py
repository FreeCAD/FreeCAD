_singleton = None


def _format_credits(value, currency):
    try:
        amount = float(value)
    except (TypeError, ValueError):
        return str(value)
    if currency in ("USD", ""):
        return "$%0.2f" % amount
    return "%0.2f %s" % (amount, currency)


def _eye_icon(revealed):
    size = 16
    pixmap = QtGui.QPixmap(size, size)
    pixmap.fill(QtCore.Qt.GlobalColor.transparent)
    painter = QtGui.QPainter(pixmap)
    painter.setRenderHint(QtGui.QPainter.RenderHint.Antialiasing)
    pen = QtGui.QPen(QtGui.QColor(150, 150, 158), 1.4)
    pen.setCapStyle(QtCore.Qt.PenCapStyle.RoundCap)
    painter.setPen(pen)
    painter.setBrush(QtCore.Qt.BrushStyle.NoBrush)
    path = QtGui.QPainterPath()
    path.moveTo(2, 8)
    path.quadTo(8, 1.5, 14, 8)
    path.quadTo(8, 14.5, 2, 8)
    painter.drawPath(path)
    painter.drawEllipse(QtCore.QRectF(6, 5.5, 5, 5))
    if not revealed:
        painter.drawLine(3, 13, 13, 3)
    painter.end()
    return QtGui.QIcon(pixmap)


class _DetailsPopup(QtWidgets.QDialog):
    sign_out_requested = QtCore.Signal()

    def __init__(self, session, parent=None):
        super().__init__(parent)
        self._session = session
        self._revealed = False
        self._value_labels = {}
        self.setWindowFlags(
            QtCore.Qt.WindowType.Dialog | QtCore.Qt.WindowType.WindowStaysOnTopHint
        )
        self.setWindowModality(QtCore.Qt.WindowModality.ApplicationModal)
        self.setWindowTitle("Parashell account")
        self.setObjectName("ParashellAuthDetailsPopup")
        self._build()

    def _build(self):
        user = self._session.get("user", {})
        layout = QtWidgets.QVBoxLayout(self)
        layout.setContentsMargins(16, 16, 16, 16)
        layout.setSpacing(12)

        header = QtWidgets.QHBoxLayout()
        header.setSpacing(8)
        title = QtWidgets.QLabel(user.get("name") or "Parashell account")
        title.setTextInteractionFlags(
            QtCore.Qt.TextInteractionFlag.TextSelectableByMouse
        )
        header.addWidget(title, 1)

        self._eye_button = QtWidgets.QToolButton(self)
        self._eye_button.setAutoRaise(True)
        self._eye_button.setCursor(QtCore.Qt.CursorShape.PointingHandCursor)
        self._eye_button.setIcon(_eye_icon(self._revealed))
        self._eye_button.setToolTip("Reveal details")
        self._eye_button.clicked.connect(self._toggle_reveal)
        header.addWidget(self._eye_button, 0)
        layout.addLayout(header)

        grid = QtWidgets.QGridLayout()
        grid.setHorizontalSpacing(14)
        grid.setVerticalSpacing(8)
        rows = [
            ("Email", "email", True),
            ("Organization", "org_id", True),
        ]
        row_index = 0
        for caption, key, sensitive in rows:
            value = user.get(key, "")
            if not value and key == "org_id":
                continue
            name_label = QtWidgets.QLabel(caption)
            value_label = QtWidgets.QLabel(self._display_value(key, value, sensitive))
            value_label.setTextInteractionFlags(
                QtCore.Qt.TextInteractionFlag.TextSelectableByMouse
            )
            grid.addWidget(name_label, row_index, 0, QtCore.Qt.AlignmentFlag.AlignRight)
            grid.addWidget(value_label, row_index, 1)
            self._value_labels[key] = (value, sensitive)
            self._value_labels[key + "::label"] = value_label
            row_index += 1
        layout.addLayout(grid)

        account_box = self._build_account_box(user)
        if account_box is not None:
            layout.addWidget(account_box)

        sign_out = QtWidgets.QPushButton("Sign out", self)
        sign_out.setCursor(QtCore.Qt.CursorShape.PointingHandCursor)
        sign_out.clicked.connect(self._on_sign_out)
        layout.addWidget(sign_out)

    def _build_account_box(self, user):
        mecontrol = self._session.get("mecontrol")
        account = mecontrol.get("account") if isinstance(mecontrol, dict) else None
        if not isinstance(account, dict):
            return None
        plan = account.get("plan") if isinstance(account.get("plan"), dict) else {}
        credits = (
            account.get("credits") if isinstance(account.get("credits"), dict) else {}
        )
        info_rows = []
        name = account.get("name") or user.get("name") or ""
        if name:
            info_rows.append(("Name", name))
        plan_name = plan.get("name") or ""
        if plan_name:
            status = plan.get("status")
            label = plan_name + (" (%s)" % status if status else "")
            info_rows.append(("Plan", label))
        remaining = credits.get("remaining")
        if isinstance(remaining, (int, float)):
            currency = str(credits.get("currency") or "").upper()
            info_rows.append(("Credits", _format_credits(remaining, currency)))
        if not info_rows:
            return None
        box = QtWidgets.QFrame(self)
        box.setObjectName("ParashellAuthAccountBox")
        box.setStyleSheet(
            "QFrame#ParashellAuthAccountBox { background-color: #252525;"
            " border: 1px solid #3c3c3c; border-radius: 4px; }"
        )
        box_layout = QtWidgets.QGridLayout(box)
        box_layout.setContentsMargins(10, 8, 10, 8)
        box_layout.setHorizontalSpacing(14)
        box_layout.setVerticalSpacing(6)
        for index, (caption, value) in enumerate(info_rows):
            caption_label = QtWidgets.QLabel(caption)
            value_label = QtWidgets.QLabel(str(value))
            value_label.setTextInteractionFlags(
                QtCore.Qt.TextInteractionFlag.TextSelectableByMouse
            )
            box_layout.addWidget(
                caption_label, index, 0, QtCore.Qt.AlignmentFlag.AlignRight
            )
            box_layout.addWidget(value_label, index, 1)
        return box

    def _display_value(self, key, value, sensitive):
        if not sensitive or self._revealed:
            return value
        if key == "email":
            return auth_config.mask_email(value)
        return "\u2022" * 12

    def _toggle_reveal(self):
        self._revealed = not self._revealed
        self._eye_button.setIcon(_eye_icon(self._revealed))
        self._eye_button.setToolTip(
            "Hide details" if self._revealed else "Reveal details"
        )
        for key, payload in list(self._value_labels.items()):
            if key.endswith("::label"):
                continue
            value, sensitive = payload
            label = self._value_labels.get(key + "::label")
            if label is not None:
                label.setText(self._display_value(key, value, sensitive))

    def _on_sign_out(self):
        self.sign_out_requested.emit()
        self.close()

    def show_centered(self):
        self.adjustSize()
        screen = QtWidgets.QApplication.primaryScreen()
        if screen is not None:
            available = screen.availableGeometry()
            x = available.left() + (available.width() - self.width()) // 2
            y = available.top() + (available.height() - self.height()) // 2
            self.move(x, y)
        self.show()
        self.raise_()
        self.activateWindow()
        self.setFocus(QtCore.Qt.FocusReason.ActiveWindowFocusReason)


class AuthStatusSlot(QtCore.QObject):
    _mecontrol_ready = QtCore.Signal(object)

    def __init__(self):
        super().__init__()
        self._button = None
        self._flow = None
        self._poll_timer = None
        self._deadline = 0.0
        self._popup = None
        self._mecontrol_timer = None
        self._mecontrol_busy = False
        self._setup_alerted = False
        self._setup_prompt_label = None
        self._setup_prompt_button = None
        self._pulse_anim = None
        self._mecontrol_ready.connect(self._on_mecontrol)

    def start(self):
        main_window = FreeCADGui.getMainWindow()
        if main_window is None:
            return
        status_bar = main_window.statusBar()
        existing = main_window.findChild(
            QtWidgets.QToolButton, auth_config.STATUS_OBJECT_NAME
        )
        if existing is not None:
            self._button = existing
        else:
            button = QtWidgets.QToolButton(main_window)
            button.setObjectName(auth_config.STATUS_OBJECT_NAME)
            button.setAutoRaise(True)
            button.setCursor(QtCore.Qt.CursorShape.PointingHandCursor)
            button.setToolButtonStyle(QtCore.Qt.ToolButtonStyle.ToolButtonTextOnly)
            button.clicked.connect(self._on_click)
            status_bar.addPermanentWidget(button)
            self._button = button
        self._refresh()
        self._ensure_polling()

    def _schedule_flow_close(self, flow):
        QtCore.QTimer.singleShot(auth_config.CALLBACK_GRACE_MS, flow.close)

    def _refresh(self):
        if self._button is None:
            return
        session = auth_core.current_session()
        if session is None:
            self._button.setText("Sign in")
            self._button.setToolTip("Sign in to your Parashell account")
            self._button.setEnabled(True)
            return
        email = session.get("user", {}).get("email", "")
        if email:
            self._button.setText("Signed in as " + auth_config.mask_email(email))
        else:
            self._button.setText("Signed in")
        self._button.setToolTip("View your Parashell account details")
        self._button.setEnabled(True)

    def _on_click(self):
        if self._flow is not None:
            return
        if auth_core.is_signed_in():
            self._show_details()
        else:
            self._begin_login()

    def _begin_login(self):
        if self._flow is not None:
            return
        try:
            flow = auth_core.LoginFlow()
        except Exception as exc:
            FreeCAD.Console.PrintError(
                "Parashell Auth: could not start the sign-in flow: %s\n" % exc
            )
            return
        self._flow = flow
        self._deadline = time.monotonic() + auth_config.CALLBACK_TIMEOUT
        self._button.setText("Signing in\u2026")
        self._button.setEnabled(False)
        self._button.setToolTip("Complete the sign-in in your browser")
        try:
            webbrowser.open(flow.authorize_url(), new=2, autoraise=True)
        except Exception as exc:
            FreeCAD.Console.PrintWarning(
                "Parashell Auth: could not open the browser: %s\n" % exc
            )
        timer = QtCore.QTimer(self)
        timer.setInterval(200)
        timer.timeout.connect(self._poll_login)
        self._poll_timer = timer
        timer.start()

    def _poll_login(self):
        flow = self._flow
        if flow is None:
            return
        try:
            code = flow.poll_for_code()
        except Exception as exc:
            self._end_login(error=str(exc))
            return
        if code is None:
            if time.monotonic() >= self._deadline:
                self._end_login(error="Timed out waiting for the sign-in to complete.")
            return
        try:
            session = flow.exchange(code)
            auth_core.store().save(session)
        except Exception as exc:
            self._end_login(error=str(exc))
            return
        self._end_login(session=session)

    def _end_login(self, session=None, error=None):
        if self._poll_timer is not None:
            self._poll_timer.stop()
            self._poll_timer.deleteLater()
            self._poll_timer = None
        flow = self._flow
        self._flow = None
        if error is not None:
            if flow is not None:
                flow.set_state(auth_config.STATUS_FAILURE)
                self._schedule_flow_close(flow)
            FreeCAD.Console.PrintWarning("Parashell Auth: sign-in failed: %s\n" % error)
            self._refresh()
            main_window = FreeCADGui.getMainWindow()
            if main_window is not None:
                main_window.statusBar().showMessage(
                    "Parashell sign-in failed: " + error, 6000
                )
            return
        if flow is not None:
            flow.set_state(auth_config.STATUS_SUCCESS)
            self._schedule_flow_close(flow)
        email = (session or {}).get("user", {}).get("email", "")
        FreeCAD.Console.PrintMessage(
            "Parashell Auth: signed in%s\n" % ((" as " + email) if email else "")
        )
        self._refresh()
        self._ensure_polling()

    def _show_details(self):
        session = auth_core.current_session()
        if session is None:
            self._refresh()
            return
        main_window = FreeCADGui.getMainWindow()
        self._popup = _DetailsPopup(session, main_window)
        self._popup.sign_out_requested.connect(self._on_sign_out)
        self._popup.show_centered()

    def _on_sign_out(self):
        self._stop_polling()
        auth_core.sign_out()
        FreeCAD.Console.PrintMessage("Parashell Auth: signed out\n")
        self._refresh()

    def _ensure_polling(self):
        if not auth_core.is_signed_in():
            self._stop_polling()
            return
        if self._mecontrol_timer is None:
            timer = QtCore.QTimer(self)
            timer.setInterval(10000)
            timer.timeout.connect(self._spawn_mecontrol)
            self._mecontrol_timer = timer
        if not self._mecontrol_timer.isActive():
            self._mecontrol_timer.start()
        self._spawn_mecontrol()

    def _stop_polling(self):
        if self._mecontrol_timer is not None:
            self._mecontrol_timer.stop()
        self._mecontrol_busy = False
        self._setup_alerted = False
        self._set_setup_prompt(False)

    def _spawn_mecontrol(self):
        if self._mecontrol_busy:
            return
        if not auth_core.is_signed_in():
            self._stop_polling()
            return
        self._mecontrol_busy = True
        thread = threading.Thread(target=self._mecontrol_worker, daemon=True)
        thread.start()

    def _mecontrol_worker(self):
        try:
            token = auth_core.valid_access_token()
            if not token:
                self._mecontrol_ready.emit({"_signed_out": True})
                return
            payload = auth_core.fetch_mecontrol(token)
            self._mecontrol_ready.emit(payload)
        except Exception as exc:
            self._mecontrol_ready.emit({"_error": str(exc)})

    def _on_mecontrol(self, payload):
        self._mecontrol_busy = False
        if not isinstance(payload, dict):
            return
        if payload.get("_signed_out"):
            self._stop_polling()
            self._refresh()
            return
        if payload.get("_error"):
            return
        if not auth_core.is_signed_in():
            self._stop_polling()
            self._refresh()
            return
        auth_core.update_mecontrol(payload)
        if bool(payload.get("isSetup")):
            if not self._setup_alerted:
                FreeCAD.Console.PrintWarning(
                    "Parashell: continue setting up your account on Parashell.\n"
                )
                self._setup_alerted = True
            self._set_setup_prompt(True)
        else:
            self._setup_alerted = False
            self._set_setup_prompt(False)
        self._refresh()

    def _set_setup_prompt(self, active):
        if self._button is None:
            return
        main_window = FreeCADGui.getMainWindow()
        status_bar = main_window.statusBar() if main_window is not None else None
        if active:
            if self._setup_prompt_label is None and status_bar is not None:
                label = QtWidgets.QLabel(
                    "Continue setting up your account on Parashell"
                )
                label.setObjectName("ParashellAuthSetupPrompt")
                go = QtWidgets.QPushButton("Go")
                go.setObjectName("ParashellAuthSetupGo")
                go.setCursor(QtCore.Qt.CursorShape.PointingHandCursor)
                go.clicked.connect(self._on_setup_go)
                status_bar.addPermanentWidget(label)
                status_bar.addPermanentWidget(go)
                self._setup_prompt_label = label
                self._setup_prompt_button = go
            self._start_pulse()
        else:
            if self._setup_prompt_label is not None:
                if status_bar is not None:
                    status_bar.removeWidget(self._setup_prompt_label)
                self._setup_prompt_label.deleteLater()
                self._setup_prompt_label = None
            if self._setup_prompt_button is not None:
                if status_bar is not None:
                    status_bar.removeWidget(self._setup_prompt_button)
                self._setup_prompt_button.deleteLater()
                self._setup_prompt_button = None
            self._stop_pulse()

    def _on_setup_go(self):
        try:
            webbrowser.open(auth_config.app_setup_url(), new=2, autoraise=True)
        except Exception as exc:
            FreeCAD.Console.PrintWarning(
                "Parashell Auth: could not open the browser: %s\n" % exc
            )

    def _start_pulse(self):
        if self._button is None:
            return
        if self._pulse_anim is None:
            anim = QtCore.QVariantAnimation(self)
            anim.setStartValue(0.0)
            anim.setKeyValueAt(0.5, 1.0)
            anim.setEndValue(0.0)
            anim.setDuration(1500)
            anim.setLoopCount(-1)
            anim.valueChanged.connect(self._apply_pulse)
            self._pulse_anim = anim
        if self._pulse_anim.state() != QtCore.QAbstractAnimation.State.Running:
            self._pulse_anim.start()

    def _stop_pulse(self):
        if self._pulse_anim is not None:
            self._pulse_anim.stop()
        if self._button is not None:
            self._button.setStyleSheet("")

    def _apply_pulse(self, value):
        if self._button is None:
            return
        try:
            t = float(value)
        except (TypeError, ValueError):
            t = 0.0
        low = (150, 95, 30)
        high = (245, 170, 55)
        red = int(low[0] + (high[0] - low[0]) * t)
        green = int(low[1] + (high[1] - low[1]) * t)
        blue = int(low[2] + (high[2] - low[2]) * t)
        self._button.setStyleSheet(
            "QToolButton { color: rgb(%d, %d, %d); }" % (red, green, blue)
        )


def _start():
    global _singleton
    if _singleton is not None:
        return
    if FreeCADGui.getMainWindow() is None:
        QtCore.QTimer.singleShot(500, _start)
        return
    _singleton = AuthStatusSlot()
    _singleton.start()


def install():
    QtCore.QTimer.singleShot(2000, _start)
