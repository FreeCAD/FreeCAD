#include "BRepFont.h"


using namespace Text;


// pre-defined font rendering options
static const unsigned int FONT_SIZE      = 72;
static const unsigned int RESOLUTION_DPI = 4800;

// compute scaling factor for specified font size
static Standard_Real getScale(const Standard_Real size)
{
    return size / Standard_Real(FONT_SIZE) * 72.0 / Standard_Real(RESOLUTION_DPI);
}


void BRepFont::setSize(const Standard_Real size)
{
    myScaleUnits = getScale(size);
    myCache.Clear();
}
