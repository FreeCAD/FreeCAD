from . import template
import sys
from pathlib import Path
import model.generateTools


class TemplateModulePyExport(template.ModelTemplate):

    def Generate(self):
        encoding = sys.getfilesystemencoding()
        exportName = self.export.Name
        inputDir = Path(self.inputDir)
        outputDir = Path(self.outputDir)

        def escapeString(s, indent=4):
            if not s:
                return None
            s = s.strip()
            s = s.replace("\\", "\\\\")
            s = s.replace('"', '\\"')
            s = s.replace("\n", f'\\n"\n{" " * indent}"')
            return s

        def docString(doc, indent=4):
            if not doc or not doc.UserDocu:
                return ""
            return escapeString(doc.UserDocu, indent) or ""

        print("TemplateModulePyExport", outputDir / exportName)

        outputDir.mkdir(parents=True, exist_ok=True)

        outputImp = outputDir / f"{exportName}ModulePyImp.cpp"
        inputImp = inputDir / f"{exportName}ModulePyImp.cpp"
        if not outputImp.exists():
            if not inputImp.exists():
                file = outputImp.open("wb")
                print("TemplateModulePyExport", "TemplateImplement", file.name)
                model.generateTools.replace(self.TemplateImplement, locals(), file)
                file.close()

        outputCpp = outputDir / f"{exportName}ModulePy.cpp"
        with outputCpp.open("wb") as file:
            print("TemplateModulePyExport", "TemplateModule", file.name)
            model.generateTools.replace(self.TemplateModule, locals(), file)

        outputHeader = outputDir / f"{exportName}ModulePy.h"
        with outputHeader.open("wb") as file:
            print("TemplateModulePyExport", "TemplateHeader", file.name)
            model.generateTools.replace(self.TemplateHeader, locals(), file)

